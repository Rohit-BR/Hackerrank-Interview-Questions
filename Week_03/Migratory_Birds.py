def migratoryBirds(arr):
    # Write your code here
    arr = sorted(arr)
    types = [0 for i in range(5)]
    
    for i in range(len(arr)):
        types[arr[i]-1] += 1
        
    max_type = types.index(max(types)) + 1
    
    return max_type


